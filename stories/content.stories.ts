export const blogPost = () => `
  <article class="content content-container">
    <header>
      <h1>How to listen to music</h1>
      <time datetime="2018-04-28T11:03:00+02:00">Saturday, April 28, 2018</time>
    </header>
    <p>
      One of the things that makes me most grateful for technology is the availability of content. The
      years of work of people long dead before you learned about them, the biggest achievements of
      infinitely inspired people, the rich life experience of the wisest artists and philosophers,
      translated to a piece of art; nowadays, these are all available to us in a one-click checkout,
      at the mercy of our speed typing their names on our keyboards. Music can be obtained as easily
      as it can be dismissed.
    </p>

    <p>
      The way things are, it’s become hard to be a responsible music listener. Correct me if I’m
      wrong, but there was a time when listening to a piece of music was a once-in-a-lifetime
      experience. Can you imagine yourself in that situation? A room full of people listening to the
      composer play their piece for the first time in public. To them, maybe not only the piece is
      new: maybe they’ve never heard the sound of a piano. When the music is over, they leave
      wondering what exactly it was they listened to, and if they’re ever going to hear it once more.
    </p>

    <p>
      My view of the world might be a bit skewed, but here goes a bold statement: listening to music,
      and learning how to listen to music is one of the most important and worthwhile things on which
      you can spend your time. My intention with this post is to stir up your curiosity and hopefully
      inspire you to start listening to music in an active way; I promise, there is a bit of effort
      involved, but the reward is only as great as you can imagine.
    </p>
    <hr />
    <h2 id="spend-some-time-together-with-it">
      Spend some time together with it
      <a href="#spend-some-time-together-with-it" class="heading-anchor">#</a>
    </h2>

    <p>
      Music can be used in many different ways, and one piece of music means different things to
      different people. One day everything’s gone awry and you come home beaten to the ground, but you
      turn on your computer and find solace in the first music that comes to your mind. Suddenly,
      you’re somewhere else, you physically feel better. Another day, you’re together with a group of
      people, and you wonder: what music could build a comfortable atmosphere, where everyone feels
      like talking to each other? While working or studying, you turn on some instrumentals that help
      you concentrate and give your brain some place it can go for a short time when its attention
      span is over.
    </p>

    <p>
      In all those situations, you’re rewarded enormously by music. But there’s something you
      definitely know but maybe haven’t done in a while: choose a band’s album or a composer’s group
      of works, lay down with a good pair of headphones, close your eyes, crank up the volume and pay
      attention to what you’re listening to. As if you were trying to meditate, focus on whatever part
      of the sound catches your attention. Let yourself stop thinking critically and become a child.
      Be carried away by whichever feeling you’re being transmitted, and listen. Try to listen as if
      you were never going to be able to hear it again. Make use of the fact that you’re hardwired to
      respond emotionally to different combinations of sound and silence; don’t be scared, because
      when you’re done, you’ll still be lying in bed, unscathed.
    </p>

    <p>
      The more you give yourself to a piece of music, the more you’ll get back from it. When you’re
      done listening, try to learn something new about the author. Was there a reason behind this
      composition? Could you figure it out without any research? This goes for music without text too:
      what’s the hidden meaning? Listen to it again, and maybe you’ll find it more wonderful than last
      time. You don’t need to know anything, no theory, no technique, no proficiency in any instrument
      will help you: the responsibility of listening to music lies exclusively in your ears and your
      brain.
    </p>
    <hr />
    <h2 id="get-rid-of-your-type-of-music">
      Get rid of “your type of music”
      <a href="#get-rid-of-your-type-of-music" class="heading-anchor">#</a>
    </h2>

    <p>
      And with this, I don’t mean “stop listening to what you like”, but “listen to as much as you
      can”.
    </p>

    <p>
      There’s a sea of compositions you can discover if you have the time, but also if you’re willing
      to detach yourself of any prejudice related to its genre, age, or relationship to politics or
      religion. Today, music is only a part of the musical product: try not to buy into aesthetics,
      personalities or attitudes. Of course, it’s fine if you enjoy a genre of music because artists
      in it are politically involved in a way you are, too. But if you’re in for the music, try to
      forget about that: it’s only going to disturb your vision of it. Nobody’s going to judge you,
      and if they do, it’s at their loss.
    </p>

    <p>
      Do not dismiss a piece of music without listening to most of it. Sometimes, the best comes at
      the end. Develop an ability to discern music composed with the heart from music composed with
      any sort of interest. Try to spot any pretentiousness in the way things are laid out. Admire
      effort and humility: these can be easily spotted, too.
    </p>

    <p>
      Try to forget about any concept of “good music” you have. If you enjoy it for the right reasons,
      then it’s good music. If you sharpen, or completely change the tools you use to choose the music
      you listen to, you’ll find there’s not enough time to listen to all the things you like.
    </p>
    <hr />
    <h2 id="discover-and-revisit-the-old">
      Discover and revisit the old
      <a href="#discover-and-revisit-the-old" class="heading-anchor">#</a>
    </h2>

    <p>
      The compositions that still reverberate today do so for a reason. When I listen to some of my
      favorites, I can’t help but think their music was there already before they found it, and their
      job was just to write it down. Some manage to distill human emotion down to its purest form, and
      translate it to paper: how they do something like that is a mystery to me. Others compose with a
      clear theme: you’d think you could guess the name of the composition by just listening to it.
    </p>

    <p>
      There’s something in common between classics: you could very easily come up with a beautiful
      reason why the composer wrote a piece. There’s a sense of purpose in the way many of them sound,
      and I can’t tell whether it’s craftsmanship, passion, greatness or some sort of divine
      intervention (it’s hard for an atheist).
    </p>

    <p>
      A lot of music sounds sad, and you’ll find classics are often sad. Don’t be scared: you can feel
      happy listening to sad music. In fact, for some reason, sad music is one of the things that
      makes me happiest. There’s a sweetness in the way sad music is touching, you can think of it as
      a moment of indulgence, where you have the chance to forgive yourself: some sort of cleansing
      mechanism.
    </p>
    <hr />

    <p>
      Do not take music for granted. It’s out there somewhere, waiting for you, and of all the ways
      you can go about feeling good, it’s the lowest-hanging fruit. Seize the opportunity, and give
      yourself the pleasure of really listening to it.
    </p>
  </article>
`

